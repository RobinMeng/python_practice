import math

## 绝对值
print(abs(-1))

## 求和
print(sum([1, 2]))

## max
print(max(range(10)))
print(min(range(7)))

## 数值的n次方

print(2 ** 3)
print(pow(2, 3))

print("*" * 40)
## e 的 x次方
print(math.exp(2))
print(math.log(8, 2))
print("*" * 40)

## 数学常量

print(math.pi)
print(math.e)
print(math.inf)
print(math.nan)
